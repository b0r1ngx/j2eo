public class ComparisonOperators {
    public static void main(String[] args) {
        float a = .5f, b = 1.6f;
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println("passed");
    }    
}
